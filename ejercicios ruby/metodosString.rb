#ejemplo de metodos de ruby
texto = "hola mundo"

texto.length #me dice de cuantos caracteres tiene la cadena
texto.reverse #muestra el texto al revez
texto.upcase #pone todo el texto en mayuscula
texto.downcase #pone todo el texto en minuscula
texto.capitalize #pone la primera letra del texto en mayuscula
texto.gsub("h""H") #reemplaza todas las h minusculas por h mayuscula
texto.include?("hola") #busca la palabra en la cadena de texto
